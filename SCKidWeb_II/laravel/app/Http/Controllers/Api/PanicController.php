    {
        //Dot syntax for digging deeper in array
        $id = $request->input('kid.id');
        $panics = $request->input('kid.panics');

        $kid = Kid::find($id);
        $kid->panics()->createMany($panics);

        return Response::json(
            array(
                'kid' => $kid,
                'panics' => $panics,
                'status' => 'Success Added Panics!')
        );
